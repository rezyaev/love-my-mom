/**
 * Randomizes position of every item in the array
 * @template T
 * @param {T[]} array
 * @returns {T[]}
 */
const shuffle = (array) => {
	if (array.length === 1) {
		return array;
	}

	const randomNumber = Math.floor(Math.random() * array.length);
	return [
		array[randomNumber],
		...shuffle(array.filter((item, index) => index != randomNumber))
	];
};

